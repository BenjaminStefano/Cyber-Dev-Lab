/* Dropeamos todas las tablas existentes para evitar duplicidades y confusiones */

DROP TABLE CONVENIOS CASCADE CONSTRAINTS;
DROP TABLE PROPIEDADES CASCADE CONSTRAINTS;
DROP TABLE INMOBILIARIAS CASCADE CONSTRAINTS;
DROP TABLE CLIENTES CASCADE CONSTRAINTS;

/* Creacion de Tablas y sus respectivos atributos, PK y FK */

CREATE TABLE CLIENTES (
    ID_CLIENTE NUMBER(10) PRIMARY KEY,
    APELLIDO_PATERNO VARCHAR2(50) NOT NULL,
    NOMBRE VARCHAR2(50) NOT NULL,
    ESTADO_CIVIL VARCHAR2(20) NOT NULL,
    RENTA NUMBER(10,0) NOT NULL
);

CREATE TABLE PROPIEDADES (
    ID_PROPIEDAD NUMBER(10) PRIMARY KEY,
    DIRECCION VARCHAR2(100) NOT NULL,
    TIPO VARCHAR2(30) NOT NULL,
    PRECIO_ARRIENDO NUMBER(10,0) NOT NULL,
    ID_CLIENTE NUMBER(10) NOT NULL,

    CONSTRAINT FK_PROPIEDADES_CLIENTES
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES(ID_CLIENTE)
);

CREATE TABLE INMOBILIARIAS (
    ID_INMOBILIARIA NUMBER(10) PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CONTACTO VARCHAR2(100) NOT NULL
);

CREATE TABLE CONVENIOS (
    ID_CONVENIO NUMBER(10) PRIMARY KEY,
    ID_INMOBILIARIA NUMBER(10) NOT NULL,
    FECHA_INICIO DATE NOT NULL,
    FECHA_FIN DATE NOT NULL,

    CONSTRAINT FK_CONVENIOS_INMOBILIARIAS
        FOREIGN KEY (ID_INMOBILIARIA)
        REFERENCES INMOBILIARIAS(ID_INMOBILIARIA)
);


