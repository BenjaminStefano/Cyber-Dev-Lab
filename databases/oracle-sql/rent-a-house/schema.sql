/* Consulta para PROPIEDADES con CLIENTE asociado */

SELECT
    P.ID_PROPIEDAD,
    P.DIRECCION,
    P.TIPO,
    P.PRECIO_ARRIENDO,
    C.ID_CLIENTE,
    C.NOMBRE,
    C.APELLIDO_PATERNO
FROM PROPIEDADES P
INNER JOIN CLIENTES C
ON P.ID_CLIENTE = C.ID_CLIENTE
ORDER BY P.TIPO, P.DIRECCION;

/* Consultar CONVENIOS vigentes con sus INMOBILIARIA */

SELECT
    CV.ID_CONVENIO,
    I.NOMBRE AS INMOBILIARIA,
    CV.FECHA_INICIO,
    CV.FECHA_FIN
FROM CONVENIOS CV
INNER JOIN INMOBILIARIAS I
ON CV.ID_INMOBILIARIA = I.ID_INMOBILIARIA
WHERE CV.FECHA_FIN >= SYSDATE;

